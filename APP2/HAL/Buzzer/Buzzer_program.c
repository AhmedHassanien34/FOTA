/******************************************************************************/
/* Syntax          : void HLED_voidTurnOn(uint8_t Copy_u8PORT , uint8_t Copy_u8PIN)
* Description      : Turn On LED
* Sync\Async       : Synchronous
* Parameters       : Copy_u8PORT , Copy_u8PIN
* Return value:    : void
*******************************************************************************/
void HBuzzer_voidTurnOn(uint8_t Copy_u8PORT , uint8_t Copy_u8PIN)
{
	/*SET PIN HIGH*/
	GPIO_u8WritePinValue(Copy_u8PORT , Copy_u8PIN , GPIO_u8_HIGH);
}
/********************************************************************************************/
/*   END OF Function                                                                        */
/********************************************************************************************/



/******************************************************************************/
/* Syntax          : void HLED_voidTurnOff(uint8_t Copy_u8PORT , uint8_t Copy_u8PIN)
* Description      : Turn Off LED
* Sync\Async       : Synchronous
* Parameters       : Copy_u8PORT , Copy_u8PIN
* Return value:    : void
*******************************************************************************/
void HLED_voidTurnOff(uint8_t Copy_u8PORT , uint8_t Copy_u8PIN)
{
	/*SET PIN LOW*/
	GPIO_u8WritePinValue(Copy_u8PORT , Copy_u8PIN , GPIO_u8_LOW);
}
/********************************************************************************************/
/*   END OF Function                                                                        */
